#include "libft.h"

char	*ft_strdup(const char *str)
{
	size_t		len;
	size_t		i;
	char		*dup;

	len = ft_strlen(str);
	i = 0;
	dup = malloc((len + 1) * sizeof(char));
	if (!dup)
		return (NULL);
	while (str[i] != '\0')
	{
		dup[i] = str[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
